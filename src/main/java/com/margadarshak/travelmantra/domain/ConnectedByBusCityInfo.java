package com.margadarshak.travelmantra.domain;

public class ConnectedByBusCityInfo {

	private String cityName;
	private String dayOfTravel;
	private String travelDuration;

	public String getCityName() {
		return cityName;
	}
	public void setCityName(String cityName) {
		this.cityName = cityName;
	}
	public String getDayOfTravel() {
		return dayOfTravel;
	}
	public void setDayOfTravel(String dayOfTravel) {
		this.dayOfTravel = dayOfTravel;
	}
	public String getTravelDuration() {
		return travelDuration;
	}
	public void setTravelDuration(String travelDuration) {
		this.travelDuration = travelDuration;
	}
}
